    <script>
        $(document).ready(function() {
            // Add Employer
            $('#addEmployerForm').on('submit', function(e) {
                e.preventDefault();
                $.ajax({
                    url: $(this).attr('action'),
                    method: 'POST',
                    data: $(this).serialize(),
                    success: function(response) {
                        alert('Employer added successfully!');
                        location.reload(); // Reload the page to see the new employer
                    },
                    error: function(response) {
                        alert('Error adding employer.');
                    }
                });
            });

            // Add Employee
            $('#addEmployeeForm').on('submit', function(e) {
                e.preventDefault();
                var formData = new FormData(this);
                $.ajax({
                    url: $(this).attr('action'),
                    method: 'POST',
                    data: formData,
                    processData: false,
                    contentType: false,
                    success: function(response) {
                        alert('Employee added successfully!');
                        location.reload(); // Reload the page to see the new employee
                    },
                    error: function(response) {
                        alert('Error adding employee.');
                    }
                });
            });
        });
    </script>
